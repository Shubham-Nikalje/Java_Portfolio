package com.jspider.musicplayer.entity;

public class Song {
	
	private int id;
	private String title;
	private String singer;
	private double duration;	


public int getid(){
	return id;
}
public void setid(int id) {
	this.id=id;
}
public String gettitle() {
	return title;	
}
public String getsinger() {
	return singer;
}
public double duration () {
	return duration;
}
}
